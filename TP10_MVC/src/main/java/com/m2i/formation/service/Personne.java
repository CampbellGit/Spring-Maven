package com.m2i.formation.service;

public class Personne {
int taille, poids;

public int getTaille() {
	return taille;
}

public void setTaille(int taille) {
	this.taille = taille;
}

public int getPoids() {
	return poids;
}

public void setPoids(int poids) {
	this.poids = poids;
}

public Personne() {
	super();
	this.taille = taille;
	this.poids = poids;
}

}
